<h1>Weather API</h1>

<p>
    The api can be reached on the <a href="../weather-api">weather-api/</a> route.
    Supply it with an IP or lat, long via the GET parameter `locationIndicator`
    <br><br>
    Here are a few example links:
    <ul>
        <li>
            <a href="../weather-api/?locationIndicator=1.1.1.1">1.1.1.1 (Cloudflare DNS)</a>
        </li>
        <li>
            <a href="../weather-api/?locationIndicator=8.8.8.8">8.8.8.8 (Google DNS)</a>
        </li>
        <li>
            <a href="../weather-api/?locationIndicator=194.47.150.9">194.47.150.9 (dbwebb.se)</a>
        </li>
    </ul>
</p>
